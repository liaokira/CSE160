function convertCoordinatesEventToGL(ev) {
  var x = ev.clientX; // x coordinate of a mouse pointer
  var y = ev.clientY; // y coordinate of a mouse pointer
  var rect = ev.target.getBoundingClientRect();

  x = ((x - rect.left) - canvas.width/2)/(canvas.width/2);
  y = (canvas.height/2 - (y - rect.top))/(canvas.height/2);

  return [x, y];
}

var g_eye = [0, 0, 3];
var g_at = [0, 0, -100];
var g_up = [0, 1, 0];

function handleKeyDown(ev) {
  const moveSpeed = 0.1;
  const rotateSpeed = 5 * Math.PI / 180;

  let forward = [
    g_at[0] - g_eye[0],
    g_at[1] - g_eye[1],
    g_at[2] - g_eye[2]
  ];
  let right = [
    forward[2], 0, -forward[0]  // perpendicular to forward vector in XZ plane
  ];

  const lenF = Math.hypot(forward[0], forward[1], forward[2]);
  const lenR = Math.hypot(right[0], right[1], right[2]);

  forward = forward.map(f => f / lenF);
  right = right.map(r => r / lenR);

  switch (ev.key) {
    case 'w':
    case 'W':
      for (let i = 0; i < 3; i++) {
        g_eye[i] += forward[i] * moveSpeed;
        g_at[i] += forward[i] * moveSpeed;
      }
      break;
    case 's':
    case 'S':
      for (let i = 0; i < 3; i++) {
        g_eye[i] -= forward[i] * moveSpeed;
        g_at[i] -= forward[i] * moveSpeed;
      }
      break;
    case 'd':
    case 'D':
      for (let i = 0; i < 3; i++) {
        g_eye[i] -= right[i] * moveSpeed;
        g_at[i] -= right[i] * moveSpeed;
      }
      break;
    case 'a':
    case 'A':
      for (let i = 0; i < 3; i++) {
        g_eye[i] += right[i] * moveSpeed;
        g_at[i] += right[i] * moveSpeed;
      }
      break;
    case 'q':
    case 'Q':
      rotateView(-rotateSpeed);
      break;
    case 'e':
    case 'E':
      rotateView(rotateSpeed);
      break;
  }

  renderAllShapes();
}

function rotateView(angle) {
  const dx = g_at[0] - g_eye[0];
  const dz = g_at[2] - g_eye[2];

  const newDx = dx * Math.cos(angle) - dz * Math.sin(angle);
  const newDz = dx * Math.sin(angle) + dz * Math.cos(angle);

  g_at[0] = g_eye[0] + newDx;
  g_at[2] = g_eye[2] + newDz;
}

function onMove(dx, dy) {
  const sensitivity = 0.5;
  g_globalAngleYFromDrag += dx * sensitivity;
  g_globalAngleXFromDrag += dy * sensitivity;
}


