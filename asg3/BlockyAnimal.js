function drawTree(batch, batch2, x, y, z, w, h) {
  // --- Draw Trunk ---

  for (let i = 0; i < h; i++) {
    batch.addCube(x, y + i, z);
  }

  // --- Draw Leaves (pyramid shape) ---
  const leafBaseY = y + h;

  for (let layer = 0; layer < w; layer++) {
    const size = w - layer; // current width of the layer
    const yOffset = leafBaseY + layer;

    for (let dx = -Math.floor(size / 2); dx <= Math.floor(size / 2); dx++) {
      for (let dz = -Math.floor(size / 2); dz <= Math.floor(size / 2); dz++) {
        batch2.addCube(x + dx, yOffset, z + dz);
      }
    }
  }
}


